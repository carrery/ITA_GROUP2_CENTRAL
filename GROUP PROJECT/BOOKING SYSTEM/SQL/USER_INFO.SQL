CREATE TABLE USER_INFO(
	USERNAME VARCHAR2(100) NOT NULL PRIMARY KEY,
	PASSWORD VARCHAR2(100) NOT NULL,
	ROLE VARCHAR2(50) NOT NULL,
	FIRSTNAME VARCHAR2(100),
	LASTNAME VARCHAR2(100),
	EMAIL VARCHAR2(100),
	CONTACT_NO VARCHAR2(100),
	IS_DELETED NUMBER(1)
);

CREATE TABLE ROLES(
	ROLE VARCHAR2(50) NOT NULL PRIMARY KEY,
	ACCESS_CB NUMBER(1) NOT NULL,
	ACCESS_FS NUMBER(1) NOT NULL,
	ACCESS_UM NUMBER(1) NOT NULL
);